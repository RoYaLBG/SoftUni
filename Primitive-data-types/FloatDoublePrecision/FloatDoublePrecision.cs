using System;

class FloatDoublePrecision
{
    static void Main(string[] args)
    {
        double PreciseDouble = 34.56789023;
        float PreciseFloat = 12.345f;
        double BigDouble = 8923.1234857;
        float BigEnoughFloat = 3456.091f;

        Console.WriteLine(PreciseDouble);
        Console.WriteLine(PreciseFloat);
        Console.WriteLine(BigDouble);
        Console.WriteLine(BigEnoughFloat);
    }
}

